USE YOMLUN1S , ONLY : NPOSRES  ,NULOUT ,RMISS
USE YOMLOG1S , ONLY : NDIMCDF,LNCSNC
USE YOETHF   , ONLY : RHOH2O
USE YOMDYN1S , ONLY : NSTEP    ,TSTEP
USE YOMCC1S  , ONLY : VCALB,VCLAIL,VCLAIH,VCFWET,VCAVGPAR
USE YOMGP1S0 , ONLY : TSLNU0   ,QLINU0   ,TILNU0   ,FSNNU0   ,&
            &TSNNU0   ,ASNNU0   ,RSNNU0   ,WRENU0   ,TRENU0,&
            &TLICENU0,TLMNWNU0,TLWMLNU0,TLBOTNU0,TLSFNU0,& !FLAKE
            &HLICENU0,HLMLNU0, &                           !FLAKE 
            &LAINU0,BSTRNU0, BSTR2NU0,WSNNU0

USE YOMGPD1S , ONLY : VFZ0F    ,VFZ0H     ,VFITM    ,VFCVL &
                     &,VFCVH   ,VFCUR     ,VFTVL  ,VFCO2TYP  ,VFISOP_EP, VFTVH    ,VFSST &
                     &,VFCI     ,VFCIL, VFSOTY    ,VFSDOR  ,VFGEO &
                     &,VDIEWSSTL ,VDINSSSTL,VDISSHFTL &
                     &,VDIETL   ,VDTSKTL,VFLDEPTH,VFCLAKE,VFCLAKEF &
                     &,VFR0VT   ,VDANDAYVT,VDANFMVT &
                     &,VDRESPBSTR,VDRESPBSTR2,VDBIOMASS_LAST&
            	     &,VFPGLOB

USE YOMDPHY  , ONLY : NLAT     ,NLON      ,NPOI     ,NPOIOFF     ,NTILES &
                     &,NLALO     ,NCSS &
                     &,NVHILO, NCLIMDAT,NCSNEC,NPOIP

USE YOESOIL1S, ONLY : RDAW
USE YOMRIP   , ONLY : RSTATI
USE YOMGC1S  , ONLY : LMASK
USE YOEPHY   , ONLY : LEVGEN   ,LESSRO, LEFLAKE, LECTESSEL,LESNML, LEC4MAP
USE MPL_MODULE


IMPLICIT NONE

!* Netcdf Interface
INTEGER NPOS,IERR,NSTPP,NVARID
REAL(KIND=JPRD) ZTIME
INTEGER ISTART1(1),ISTART2(2),ISTART3(3),ISTART4(4)
INTEGER ICOUNT1(1),ICOUNT2(2),ICOUNT3(3),ICOUNT4(4)
REAL(KIND=JPRD),ALLOCATABLE :: ZMASK(:)
REAL(KIND=JPRD),ALLOCATABLE :: ZOUTPUT(:,:)

!* Local declarations
REAL(KIND=JPRB),ALLOCATABLE :: ZQLINU0(:,:)
INTEGER(KIND=JPIM) :: IX1,IX2,IY1,IY2,NLEN,IST,IEN,ILEVS,JM,JTILE,JK,JVT,ISTP,IENP
REAL(KIND=JPRB),ALLOCATABLE ::ZBUF(:)
REAL(KIND=JPRB),ALLOCATABLE ::ZVALUE(:)
INTEGER(KIND=JPIM) :: MYPROC, NPROC
REAL(KIND=JPHOOK)    :: ZHOOK_HANDLE
INTEGER(KIND=JPIM) :: NMXNV

#include "netcdf.inc"
#include "minmax.intfb.h"

IF (LHOOK) CALL DR_HOOK('WRTRES',0,ZHOOK_HANDLE)

MYPROC = MPL_MYRANK()
NPROC  = MPL_NPROC()

ISTP   = NPOIOFF(1)+1
IENP   = NPOIOFF(NPROC+1)

WRITE(NULOUT,*) 'WRTDRES'


!     ------------------------------------------------------------------

!*       1.   SCALING SOME VARIABLES.
!             -----------------------

!     NOTE: UNITS OF FSNNU,WRENU IN THE SURF1D MODEL:            KG/M2
!           UNITS OF SNOW AND INTERCEP RESER WATER IN THE
!                                     PHYSICS:                   KG/M2
!           UNITS OF SNOW AND INTERCEP RESER WATER IN THE
!                                     INPUT AND OUTPUT FILES:    KG/M2=MM

IF(NDIMCDF == 2)THEN
  IX1=1
  IX2=NLON
  IY1=1
  IY2=NLAT
  NLEN=(IX2-IX1+1)*(IY2-IY1+1)
  IST=IX1+(IY1-1)*NLON
  IEN=IST+NLEN-1
ELSE
  IST=1
  IEN=NLALO
  NLEN=IEN-IST+1
ENDIF

! max size for second dimension of output so that we can reuse ZOUTPUT
NMXNV=MAX(NCLIMDAT,MAX(MAX(MAX(NTILES,NCSS),NCSNEC),NVHILO))

ALLOCATE (ZOUTPUT(NLEN,NMXNV))            !INFLATED OUTPUT ARRAY
!ALLOCATE (ZOUTPUTA(NLEN,NCLIMDAT))  !INFLATED OUTPUT ARRAY
!ALLOCATE (ZOUTPUTT(NLEN,NTILES))
!ALLOCATE (ZOUTPUTS(NLEN,NCSS))
!ALLOCATE (ZOUTPUTSN(NLEN,NCSNEC))
!ALLOCATE (ZOUTPUTV(NLEN,NVHILO))


ALLOCATE (ZVALUE(NPOI))
ALLOCATE (ZQLINU0(NPOI,NCSS))
DO ILEVS=1,NCSS
  ZQLINU0(:,ILEVS)=QLINU0(:,ILEVS)*RDAW(ILEVS)*RHOH2O
ENDDO

ALLOCATE (ZBUF(NLEN))

NPOS = NPOSRES

!* -- write spatial and time coordinates

!     time
ISTART1(1)=1
ZTIME=RSTATI-TSTEP*0.5_JPRB
IF( MYPROC == 1 ) THEN
  NVARID = NCVID(NPOS,'time',IERR)
  CALL NCVPT1 (NPOS,NVARID,ISTART1,ZTIME,IERR)
  NVARID = NCVID(NPOS,'timestp',IERR)
  NSTPP=NSTEP
  CALL NCVPT1 (NPOS,NVARID,ISTART1,NSTPP,IERR)
ENDIF
!* -- Surface characteristics
!***********************************************************************
IF(NDIMCDF == 2)THEN
  ISTART2(1)=IX1
  ISTART2(2)=IY1
  ICOUNT2(1)=IX2-IX1+1
  ICOUNT2(2)=IY2-IY1+1
ELSE
  ISTART2(1)=IST
  ICOUNT2(1)=NLEN
  ICOUNT2(2)=1
ENDIF

!     catchment mask
ALLOCATE (ZMASK(NLEN))
ZMASK(:)=RMISS
WHERE (LMASK(ISTP:IENP))
  ZMASK=1._JPRB
END WHERE

IF( MYPROC == 1 ) THEN
  NVARID = NCVID(NPOS,'Mask',IERR)
  CALL NCVPT (NPOS,NVARID,ISTART2,ICOUNT2,ZMASK,IERR)
ENDIF

DEALLOCATE(ZMASK)

!     momentum roughness
CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=VFZ0F(:),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:z0m")
IF( MYPROC == 1 ) THEN
  NVARID = NCVID(NPOS,'z0m',IERR)
  ZOUTPUT(:,1)=UNPACK(ZBUF(:),LMASK(ISTP:IENP),RMISS)
  CALL NCVPT (NPOS,NVARID,ISTART2,ICOUNT2,ZOUTPUT(:,1),IERR)
ENDIF

!     heat roughness
ZVALUE(:)=LOG(VFZ0H(:))
CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=ZVALUE(:),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:lz0h")
IF( MYPROC == 1 ) THEN
  NVARID = NCVID(NPOS,'lz0h',IERR)
  ZOUTPUT(:,1)=UNPACK(ZBUF(:),LMASK(ISTP:IENP),RMISS)
  CALL NCVPT (NPOS,NVARID,ISTART2,ICOUNT2,ZOUTPUT(:,1),IERR)
ENDIF
DEALLOCATE (ZVALUE)

!     monthly albedo
IF(NDIMCDF == 2)THEN
  ISTART3(1)=IX1
  ISTART3(2)=IY1
  ISTART3(3)=1
  ICOUNT3(1)=IX2-IX1+1
  ICOUNT3(2)=IY2-IY1+1
  ICOUNT3(3)=NCLIMDAT
ELSE
  ISTART3(1)=IST
  ISTART3(2)=1
  ICOUNT3(1)=NLEN
  ICOUNT3(2)=NCLIMDAT
ENDIF
DO JM=1,NCLIMDAT
  CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=VCALB(:,JM),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:Malbedo")
  IF( MYPROC == 1 ) THEN
    ZOUTPUT(:,JM)=UNPACK(ZBUF(:),LMASK(ISTP:IENP),RMISS)
  ENDIF
ENDDO

IF( MYPROC == 1 ) THEN
  NVARID = NCVID(NPOS,'Malbedo',IERR)
  CALL NCVPT (NPOS,NVARID,ISTART3,ICOUNT3,ZOUTPUT,IERR)
ENDIF

! LAI low veg
DO JM=1,NCLIMDAT
  CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=VCLAIL(:,JM),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:Mlail")
  IF( MYPROC == 1 ) THEN
    ZOUTPUT(:,JM)=UNPACK(ZBUF(:),LMASK(ISTP:IENP),RMISS)
  ENDIF
ENDDO

IF( MYPROC == 1 ) THEN
  NVARID = NCVID(NPOS,'Mlail',IERR)
  CALL NCVPT (NPOS,NVARID,ISTART3,ICOUNT3,ZOUTPUT,IERR)
ENDIF

! LAI high veg
DO JM=1,NCLIMDAT
  CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=VCLAIH(:,JM),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:Mlaih")
  IF( MYPROC == 1 ) THEN
    ZOUTPUT(:,JM)=UNPACK(ZBUF(:),LMASK(ISTP:IENP),RMISS)
  ENDIF
ENDDO

IF( MYPROC == 1 ) THEN
  NVARID = NCVID(NPOS,'Mlaih',IERR)
  CALL NCVPT (NPOS,NVARID,ISTART3,ICOUNT3,ZOUTPUT,IERR)
ENDIF


! wetland fraction
DO JM=1,NCLIMDAT
  CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=VCFWET(:,JM),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:Fwet")
  IF( MYPROC == 1 ) THEN
    ZOUTPUT(:,JM)=UNPACK(ZBUF(:),LMASK(ISTP:IENP),RMISS)
  ENDIF
ENDDO

IF( MYPROC == 1 ) THEN
  NVARID = NCVID(NPOS,'fwet',IERR)
  CALL NCVPT (NPOS,NVARID,ISTART3,ICOUNT3,ZOUTPUT,IERR)
ENDIF


! average PAR
DO JM=1,NCLIMDAT
  CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=VCAVGPAR(:,JM),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:par_avg")
  IF( MYPROC == 1 ) THEN
    ZOUTPUT(:,JM)=UNPACK(ZBUF(:),LMASK(ISTP:IENP),RMISS)
  ENDIF
ENDDO

IF( MYPROC == 1 ) THEN
  NVARID = NCVID(NPOS,'par_avg',IERR)
  CALL NCVPT (NPOS,NVARID,ISTART3,ICOUNT3,ZOUTPUT,IERR)
ENDIF

!     land sea mask
CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=VFITM(:),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:landsea")
IF( MYPROC == 1 ) THEN
  NVARID = NCVID(NPOS,'landsea',IERR)
  ZOUTPUT(:,1)=UNPACK(ZBUF(:),LMASK(ISTP:IENP),RMISS)
  CALL NCVPT (NPOS,NVARID,ISTART2,ICOUNT2,ZOUTPUT(:,1),IERR)
ENDIF

!     geopotential
CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=VFGEO(:),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:geopot")
IF( MYPROC == 1 ) THEN
  NVARID = NCVID(NPOS,'geopot',IERR)
  ZOUTPUT(:,1)=UNPACK(ZBUF(:),LMASK(ISTP:IENP),RMISS)
  CALL NCVPT (NPOS,NVARID,ISTART2,ICOUNT2,ZOUTPUT(:,1),IERR)
ENDIF

!     low vegetation coverage
CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=VFCVL(:),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:cvl")
IF( MYPROC == 1 ) THEN
  NVARID = NCVID(NPOS,'cvl',IERR)
  ZOUTPUT(:,1)=UNPACK(ZBUF(:),LMASK(ISTP:IENP),RMISS)
  CALL NCVPT (NPOS,NVARID,ISTART2,ICOUNT2,ZOUTPUT(:,1),IERR)
ENDIF

!     high vegetation coverage
CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=VFCVH(:),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:cvh")
IF( MYPROC == 1 ) THEN
  NVARID = NCVID(NPOS,'cvh',IERR)
  ZOUTPUT(:,1)=UNPACK(ZBUF(:),LMASK(ISTP:IENP),RMISS)
  CALL NCVPT (NPOS,NVARID,ISTART2,ICOUNT2,ZOUTPUT(:,1),IERR)
ENDIF

!     low vegetation type
CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=VFTVL(:),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:tvl")
IF( MYPROC == 1 ) THEN
  NVARID = NCVID(NPOS,'tvl',IERR)
  ZOUTPUT(:,1)=UNPACK(ZBUF(:),LMASK(ISTP:IENP),RMISS)
  CALL NCVPT (NPOS,NVARID,ISTART2,ICOUNT2,ZOUTPUT(:,1),IERR)
ENDIF

!     high vegetation type
CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=VFTVH(:),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:tvh")
IF( MYPROC == 1 ) THEN
  NVARID = NCVID(NPOS,'tvh',IERR)
  ZOUTPUT(:,1)=UNPACK(ZBUF(:),LMASK(ISTP:IENP),RMISS)
  CALL NCVPT (NPOS,NVARID,ISTART2,ICOUNT2,ZOUTPUT(:,1),IERR)
ENDIF

!     urban coverage
CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=VFCUR(:),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:cu")
IF( MYPROC == 1 ) THEN
  NVARID = NCVID(NPOS,'cu',IERR)
  ZOUTPUT(:,1)=UNPACK(ZBUF(:),LMASK(ISTP:IENP),RMISS)
  CALL NCVPT (NPOS,NVARID,ISTART2,ICOUNT2,ZOUTPUT(:,1),IERR)
ENDIF

IF (LEC4MAP) THEN
!     CO2 photosynthetic pathway (C3/C4 vegetation type)
  CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=VFCO2TYP(:),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:Ctype")
  IF( MYPROC == 1 ) THEN
    NVARID = NCVID(NPOS,'Ctype',IERR)
!  ZOUTPUT(:,1)=UNPACK(VFCO2TYP,LMASK(IST:IEN),RMISS)
    ZOUTPUT(:,1)=UNPACK(ZBUF(:),LMASK(IST:IEN),RMISS)
    CALL NCVPT (NPOS,NVARID,ISTART2,ICOUNT2,ZOUTPUT,IERR)
  ENDIF
ENDIF

! Isoprene emission potential
CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=VFISOP_EP(:),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:ISOP_EP")
IF( MYPROC == 1 ) THEN
  NVARID = NCVID(NPOS,'ISOP_EP',IERR)
  ZOUTPUT(:,1)=UNPACK(ZBUF(:),LMASK(IST:IEN),RMISS)
  CALL NCVPT (NPOS,NVARID,ISTART2,ICOUNT2,ZOUTPUT,IERR)
ENDIF

!     soil type
CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=VFSOTY(:),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:sotype")
IF( MYPROC == 1 ) THEN
  NVARID = NCVID(NPOS,'sotype',IERR)
  ZOUTPUT(:,1)=UNPACK(ZBUF(:),LMASK(ISTP:IENP),RMISS)
  CALL NCVPT (NPOS,NVARID,ISTART2,ICOUNT2,ZOUTPUT(:,1),IERR)
ENDIF

!     orographic variance
CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=VFSDOR(:),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:sdor")
IF( MYPROC == 1 ) THEN
  NVARID = NCVID(NPOS,'sdor',IERR)
  ZOUTPUT(:,1)=UNPACK(ZBUF(:),LMASK(ISTP:IENP),RMISS)
  CALL NCVPT (NPOS,NVARID,ISTART2,ICOUNT2,ZOUTPUT(:,1),IERR)
ENDIF

!     sst
CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=VFSST(:),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:sst")
IF( MYPROC == 1 ) THEN
  NVARID = NCVID(NPOS,'sst',IERR)
  ZOUTPUT(:,1)=UNPACK(ZBUF(:),LMASK(ISTP:IENP),RMISS)
  CALL NCVPT (NPOS,NVARID,ISTART2,ICOUNT2,ZOUTPUT(:,1),IERR)
ENDIF

!     sea ice fraction
CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=VFCI(:),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:seaice")
IF( MYPROC == 1 ) THEN
  NVARID = NCVID(NPOS,'seaice',IERR)
  ZOUTPUT(:,1)=UNPACK(ZBUF,LMASK(ISTP:IENP),RMISS)
  CALL NCVPT (NPOS,NVARID,ISTART2,ICOUNT2,ZOUTPUT(:,1),IERR)
ENDIF

!     land ice fraction
CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=VFCIL(:),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:glacierMask")
IF( MYPROC == 1 ) THEN
  NVARID = NCVID(NPOS,'glacierMask',IERR)
  ZOUTPUT(:,1)=UNPACK(ZBUF,LMASK(ISTP:IENP),RMISS)
  CALL NCVPT (NPOS,NVARID,ISTART2,ICOUNT2,ZOUTPUT(:,1),IERR)
ENDIF


!     x gaussian reduced index
CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=VFPGLOB(:),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:gaussianIndex")
IF ( MYPROC == 1 ) THEN
  NVARID = NCVID(NPOS,'x',IERR)
  ZOUTPUT(:,1)=UNPACK(ZBUF,LMASK(ISTP:IENP),RMISS)
  CALL NCVPT (NPOS,NVARID,ISTART2,ICOUNT2,ZOUTPUT,IERR)
ENDIF


!* -- Tile specific quantities
!***********************************************************************
!     sensible heat flux
IF(NDIMCDF == 2)THEN
  ISTART3(1)=IX1
  ISTART3(2)=IY1
  ISTART3(3)=1
  ICOUNT3(1)=IX2-IX1+1
  ICOUNT3(2)=IY2-IY1+1
  ICOUNT3(3)=NTILES
ELSE
  ISTART3(1)=IST
  ISTART3(2)=1
  ICOUNT3(1)=NLEN
  ICOUNT3(2)=NTILES
ENDIF

DO JTILE=1,NTILES
  CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=VDISSHFTL(:,JTILE),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:Qh")
  IF( MYPROC == 1 ) THEN
    ZOUTPUT(:,JTILE)=UNPACK(ZBUF(:),LMASK(ISTP:IENP),RMISS)
    ! Write out log of restart file to check bit-repro
    WRITE(NULOUT,'(A6,I2.2)')'Qh,JT:',JTILE
    CALL MINMAX('Qh',REAL(ZOUTPUT(:,JTILE),KIND=JPRB),ICOUNT2(1),ICOUNT2(2),LMASK(ISTP:IENP),NULOUT)
  ENDIF
ENDDO

IF( MYPROC == 1 ) THEN
  NVARID = NCVID(NPOS,'Qh',IERR)
  CALL NCVPT (NPOS,NVARID,ISTART3,ICOUNT3,ZOUTPUT,IERR)
ENDIF

!     latent heat flux
DO JTILE=1,NTILES
  CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=VDIETL(:,JTILE),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:Evap")
  IF( MYPROC == 1 ) THEN
    ZOUTPUT(:,JTILE)=UNPACK(ZBUF(:),LMASK(ISTP:IENP),RMISS)
    ! Write out log of restart file to check bit-repro
    WRITE(NULOUT,'(A8,I2.2)')'Evap,JT:',JTILE 
    CALL MINMAX('Evap',REAL(ZOUTPUT(:,JTILE),KIND=JPRB),ICOUNT2(1),ICOUNT2(2),LMASK(ISTP:IENP),NULOUT)
  ENDIF
ENDDO

IF( MYPROC == 1 ) THEN
  NVARID = NCVID(NPOS,'Evap',IERR)
  CALL NCVPT (NPOS,NVARID,ISTART3,ICOUNT3,ZOUTPUT,IERR)
ENDIF

!     momentum flux u

DO JTILE=1,NTILES
  CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=VDIEWSSTL(:,JTILE),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:Momu")
  IF( MYPROC == 1 ) THEN
    ZOUTPUT(:,JTILE)=UNPACK(ZBUF(:),LMASK(ISTP:IENP),RMISS)
    ! Write out log of restart file to check bit-repro
    WRITE(NULOUT,'(A8,I2.2)')'Momu,JT:',JTILE 
    CALL MINMAX('Momu',REAL(ZOUTPUT(:,JTILE),KIND=JPRB),ICOUNT2(1),ICOUNT2(2),LMASK(ISTP:IENP),NULOUT)
  ENDIF
ENDDO

IF( MYPROC == 1 ) THEN
  NVARID = NCVID(NPOS,'Momu',IERR)
  CALL NCVPT (NPOS,NVARID,ISTART3,ICOUNT3,ZOUTPUT,IERR)
ENDIF

!     momentum flux v
DO JTILE=1,NTILES
  CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=VDINSSSTL(:,JTILE),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:Momv")
  IF( MYPROC == 1 ) THEN
    ZOUTPUT(:,JTILE)=UNPACK(ZBUF(:),LMASK(ISTP:IENP),RMISS)
    ! Write out log of restart file to check bit-repro
    WRITE(NULOUT,'(A8,I2.2)')'Momv,JT:',JTILE 
    CALL MINMAX('Momv',REAL(ZOUTPUT(:,JTILE),KIND=JPRB),ICOUNT2(1),ICOUNT2(2),LMASK(ISTP:IENP),NULOUT)
  ENDIF
ENDDO

IF( MYPROC == 1 ) THEN
  NVARID = NCVID(NPOS,'Momv',IERR)
  CALL NCVPT (NPOS,NVARID,ISTART3,ICOUNT3,ZOUTPUT,IERR)
ENDIF

!     skin temperature
IF( MYPROC == 1 ) NVARID = NCVID(NPOS,'SkinT',IERR)
DO JTILE=1,NTILES
  CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=VDTSKTL(:,JTILE),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:SkinT")
  IF( MYPROC == 1 ) THEN
    ZOUTPUT(:,JTILE)=UNPACK(ZBUF(:),LMASK(ISTP:IENP),RMISS)
    ! Write out log of restart file to check bit-repro
    WRITE(NULOUT,'(A9,I2.2)')'SkinT,JT:',JTILE 
    CALL MINMAX('SkinT',REAL(ZOUTPUT(:,JTILE),KIND=JPRB),ICOUNT2(1),ICOUNT2(2),LMASK(ISTP:IENP),NULOUT)
  ENDIF
ENDDO

IF( MYPROC == 1 ) THEN
  NVARID = NCVID(NPOS,'SkinT',IERR)
  CALL NCVPT (NPOS,NVARID,ISTART3,ICOUNT3,ZOUTPUT,IERR)
ENDIF

!*-- Prognostic quantities
!***********************************************************************
!     skin layer content
CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=WRENU0(:),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:CanopInt")
IF( MYPROC == 1 ) THEN
  ZOUTPUT(:,1)=UNPACK(ZBUF(:),LMASK(ISTP:IENP),RMISS)
  NVARID = NCVID(NPOS,'CanopInt',IERR)
  CALL NCVPT (NPOS,NVARID,ISTART2,ICOUNT2,ZOUTPUT(:,1),IERR)
  ! Write out log of restart file to check bit-repro
  CALL MINMAX('CanopInt',REAL(ZOUTPUT(:,1),KIND=JPRB),ICOUNT2(1),ICOUNT2(2),LMASK(ISTP:IENP),NULOUT)
ENDIF

!     snow mass
CALL MPL_GATHERV(PRECVBUF=ZBUF(:),KROOT=1,PSENDBUF=FSNNU0(:,1),KRECVCOUNTS=NPOIP(:),CDSTRING="WRTRES:SWE")
IF( MYPROC == 1 ) THEN
