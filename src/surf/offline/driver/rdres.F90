!**** *RDRES*   - Routine to read restart file

!     Purpose.
!     --------
!           Initialize model from restart file

!***  Interface.
!     ----------
!        *CALL* *RDRES

!     Explicit arguments :
!     --------------------
!         LOPLEFT: FLAG INDICATING WHETHER MORE POINTS NEED TO BE DONE

!     Method.
!     -------
!        See documentation

!     Externals.
!     ----------

!     Reference.
!     ----------

!     Author.
!     -------
!        Bart vd Hurk KNMI

!     Modifications.
