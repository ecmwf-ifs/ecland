MODULE ECLAND_INTERNAL_TYPE_MOD
USE PARKIND1, ONLY : JPIM, JPRB
USE YOMGPD1S, ONLY : VFDO, VFHO, VFLDEPTH, GPD_SDP2, GPD_SDP3
USE FIELD_MODULE, ONLY: FIELD_2RB, FIELD_3RB, FIELD_4RB
USE FIELD_FACTORY_MODULE

IMPLICIT NONE

TYPE INTERNAL_TYPE
  REAL(KIND=JPRB), DIMENSION(:,:),     POINTER :: PDO     => NULL()
  CLASS(FIELD_3RB), POINTER :: F_PDO     => NULL()
  REAL(KIND=JPRB), DIMENSION(:,:),     POINTER :: PHO     => NULL()
  CLASS(FIELD_3RB), POINTER :: F_PHO     => NULL()
  REAL(KIND=JPRB), DIMENSION(:),       POINTER :: PLDEPTH => NULL()
  CLASS(FIELD_2RB), POINTER :: F_PLDEPTH => NULL()
  REAL(KIND=JPRB), DIMENSION(:,:),     POINTER :: PSSDP2  => NULL()
  CLASS(FIELD_3RB), POINTER :: F_PSSDP2  => NULL()
  REAL(KIND=JPRB), DIMENSION(:,:,:),   POINTER :: PSSDP3  => NULL()
  CLASS(FIELD_4RB), POINTER :: F_PSSDP3  => NULL()
CONTAINS
  PROCEDURE :: INIT   => INTERNAL_TYPE_INIT
  PROCEDURE :: FINAL  => INTERNAL_TYPE_FINAL
  PROCEDURE :: UPDATE_VIEW => INTERNAL_TYPE_UPDATE_VIEW
  PROCEDURE :: SYNC_HOST   => INTERNAL_TYPE_SYNC_HOST
END TYPE INTERNAL_TYPE

CONTAINS

 SUBROUTINE INTERNAL_TYPE_INIT(SELF)
    CLASS(INTERNAL_TYPE) :: SELF
    CALL FIELD_NEW(SELF%F_PDO,     DATA=VFDO)
    CALL FIELD_NEW(SELF%F_PHO,     DATA=VFHO)
    CALL FIELD_NEW(SELF%F_PLDEPTH, DATA=VFLDEPTH)
    CALL FIELD_NEW(SELF%F_PSSDP2,  DATA=GPD_SDP2(:,:,:))
    CALL FIELD_NEW(SELF%F_PSSDP3,  DATA=GPD_SDP3(:,:,:,:))
 END SUBROUTINE INTERNAL_TYPE_INIT

 SUBROUTINE INTERNAL_TYPE_FINAL(SELF)
    CLASS(INTERNAL_TYPE) :: SELF
    CALL FIELD_DELETE(SELF%F_PDO)
    CALL FIELD_DELETE(SELF%F_PHO)
    CALL FIELD_DELETE(SELF%F_PLDEPTH)
    CALL FIELD_DELETE(SELF%F_PSSDP2)
    CALL FIELD_DELETE(SELF%F_PSSDP3)
    NULLIFY(SELF%PDO)
    NULLIFY(SELF%PHO)
    NULLIFY(SELF%PLDEPTH)
    NULLIFY(SELF%PSSDP2)
    NULLIFY(SELF%PSSDP3)
 END SUBROUTINE INTERNAL_TYPE_FINAL

 SUBROUTINE INTERNAL_TYPE_UPDATE_VIEW(SELF, BLOCK_INDEX)
    CLASS(INTERNAL_TYPE) :: SELF
    INTEGER(KIND=JPIM), INTENT(IN) :: BLOCK_INDEX
    IF(ASSOCIATED(SELF%F_PDO))     SELF%PDO     => SELF%F_PDO%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_PHO))     SELF%PHO     => SELF%F_PHO%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_PLDEPTH)) SELF%PLDEPTH => SELF%F_PLDEPTH%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_PSSDP2))  SELF%PSSDP2  => SELF%F_PSSDP2%GET_VIEW(BLOCK_INDEX)
    IF(ASSOCIATED(SELF%F_PSSDP3))  SELF%PSSDP3  => SELF%F_PSSDP3%GET_VIEW(BLOCK_INDEX)
 END SUBROUTINE INTERNAL_TYPE_UPDATE_VIEW

 SUBROUTINE INTERNAL_TYPE_SYNC_HOST(SELF)
    CLASS(INTERNAL_TYPE) :: SELF
    CALL SELF%F_PDO%SYNC_HOST_RDWR()
    CALL SELF%F_PHO%SYNC_HOST_RDWR()
    CALL SELF%F_PLDEPTH%SYNC_HOST_RDWR()
    CALL SELF%F_PSSDP2%SYNC_HOST_RDWR()
    CALL SELF%F_PSSDP3%SYNC_HOST_RDWR()
 END SUBROUTINE INTERNAL_TYPE_SYNC_HOST

END MODULE ECLAND_INTERNAL_TYPE_MOD
