SUBROUTINE SRFSN_REGRID(KIDIA,KFDIA,KLON,KLEVSN,LLNOSNOW,&
                    PSSN,PRSN,PWSN,PTSN,PDSNNEW,&
                    PSSNNEW,PRSNNEW,PWSNNEW,PTSNNEW)

USE PARKIND1 , ONLY : JPIM, JPRB
USE YOMHOOK  , ONLY : LHOOK, DR_HOOK, JPHOOK
USE ABORT_SURF_MOD

